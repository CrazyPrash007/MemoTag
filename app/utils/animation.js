// For section variants with inView trigger
export const createScrollTriggeredVariants = (variants, delay = 0) => {
  return {
    ...variants,
    visible: {
      ...variants.visible,
      transition: {
        ...variants.visible.transition,
        delay
      }
    }
  };
}; 